public class IncrementOperators {
    public static void main(String[] args) {
        int p=10;
        int q=p++;
        System.out.println(p);
        System.out.println(q);
        System.out.println(p);

        System.out.println(" ");
        int r=10;
        int s=++r;

        System.out.println(r);
        System.out.println(s);
        System.out.println(r);
    }
}
